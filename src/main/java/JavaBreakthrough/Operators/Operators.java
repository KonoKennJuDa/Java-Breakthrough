package JavaBreakthrough.Operators;

public class Operators {

    public static void main(String[] args) {

        // В Java операторы — это специальные символы или ключевые слова,
        // которые выполняют операции над операндами (переменными, значениями и выражениями).
        // Они делятся на несколько категорий:

        /**
         * Арифметические операторы.
         * Используются для выполнения математических операций:
         */

        // + (сложение)
        // - (вычитание)
        // * (умножение)
        // / (деление)
        // % (остаток от деления)
        // ++ (инкремент)
        // -- (декремент)

        int a = 10 + 5;  // 15
        int b = a % 3;   // 0 (остаток от деления 15 на 3)
        a++;             // Увеличивает a на 1 (постфиксный инкремент)

        /**
         * Операторы сравнения.
         * Сравнивают два значения и возвращают boolean (true или false):
         */

        // == (равно)
        // != (не равно)
        // > (больше)
        // < (меньше)
        // >= (больше или равно)
        // <= (меньше или равно)

        boolean isEqual = (5 == 5);  // true
        boolean isGreater = (10 > 20); // false

        /**
         * Логические операторы.
         * Работают с булевыми значениями (true, false):
         */

        // && (логическое И)
        // || (логическое ИЛИ)
        // ! (логическое НЕ)
        // & (И без сокращенного вычисления)
        // | (ИЛИ без сокращенного вычисления)
        // ^ (исключающее ИЛИ, XOR)

        boolean result = (true && false);  // false
        boolean xorResult = (true ^ false); // true

        /**
         * Операторы присваивания.
         * Присваивают значение переменной:
         */

        // = (присваивание)
        // += (прибавление с присваиванием)
        // -= (вычитание с присваиванием)
        // *= (умножение с присваиванием)
        // /= (деление с присваиванием)
        // %= (остаток от деления с присваиванием)

        int x = 10;
        x += 5;  // x = x + 5 → 15

        /**
         * Побитовые операторы.
         * Работают с битами чисел:
         */

        // & (побитовое И)
        // | (побитовое ИЛИ)
        // ^ (побитовое XOR)
        // ~ (побитовое НЕ)
        // << (сдвиг влево)
        // >> (сдвиг вправо с сохранением знака)
        // >>> (сдвиг вправо без сохранения знака)

        int num = 5 & 3;  // 101 & 011 = 001 → 1
        int shifted = num << 1; // 5 << 1 → 10

        /**
         * Тернарный оператор (? :)
         * Сокращенная форма условного оператора if-else:
         */

        int aa = 10;
        int bb = (aa > 5) ? 100 : 200;  // bb = 100, так как условие true

        /**
         * Оператор проверки типа (instanceof)
         * Проверяет, является ли объект экземпляром класса:
         */

        String str = "Hello";
        boolean isString = str instanceof String;  // true

        /**
         * Операторы работы с объектами.
         */

        // . (доступ к полям и методам объекта)
        // new (создание объекта)
        // [] (доступ к элементам массива)

        String s = new String("Java");
        int length = s.length();  // 4

        /**
         * Группировка и вызов методов.
         */

        // () (вызов метода, группировка выражений)
        // , (разделение выражений)


        // Приоритет операторов определяет порядок выполнения операций (например, умножение выполняется перед сложением).
        // Ассоциативность определяет направление вычисления (слева направо или справа налево).
    }
}
