package com.javabreakthrough.Lesson3.Cycles;

public class For {

    public static void main(String[] args) {

        // Пример с инкрементом
        for (int i = 0; i <= 10; i++) {
            System.out.println("Hello " + i);
        }

        // Пример с декрементом
        for (int j = 10; j >= 0 ; j--) {
            System.out.println("Hello " + j);
        }
    }
}
